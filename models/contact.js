import mongoose, { Schema } from "mongoose";

const contactSchema = new mongoose.Schema(
  {
    name: {
      type: String,
      required: [true, "Set name for contact"],
    },
    email: {
      type: String,
      required: true,
    },
    phone: {
      type: String,
      match: [
        /^\(\d{3}\) \d{3}-\d{4}$/,
        "Invalid phone number. Example (XXX) XXX-XXXX",
      ],
      required: true,
    },
    favorite: {
      type: Boolean,
      default: false,
    },
    owner: {
      type: Schema.Types.ObjectId,
      ref: "user",
    },
  },
  {
    versionKey: false,
    timestamps: true,
  }
);

export default mongoose.model("Contact", contactSchema);
